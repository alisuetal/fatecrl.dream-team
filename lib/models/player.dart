class Player {
  final int id;
  final String name;
  final String position;
  final int point;
  final int rebound;
  final int block;
  final int steal;
  final int assist;
  final int missShot;
  final int turnOver;
  final String urlImage;
  final double price;
  final String team;

  Player({
    required this.id,
    required this.name,
    required this.position,
    required this.point,
    required this.rebound,
    required this.block,
    required this.steal,
    required this.assist,
    required this.missShot,
    required this.turnOver,
    required this.urlImage,
    required this.price,
    required this.team,
  });
}
